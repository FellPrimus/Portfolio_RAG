def get_system_prompt(strategy: str = "balanced") -> str:
    """
    프롬프트 전략에 따라 시스템 프롬프트 반환

    Args:
        strategy: 'strict', 'balanced', 'few_shot' 중 하나

    Returns:
        str: 선택된 시스템 프롬프트 템플릿

    Example:
        >>> prompt = get_system_prompt("balanced")
        >>> final_prompt = prompt.format(context="...", question="...")
    """
    prompts = {
        "strict": STRICT_PROMPT,
        "balanced": BALANCED_PROMPT,
        "few_shot": FEW_SHOT_PROMPT
    }

    if strategy not in prompts:
        print(f"⚠️ 알 수 없는 프롬프트 전략: {strategy}. 'balanced'를 사용합니다.")
        strategy = "balanced"

    return prompts[strategy]


